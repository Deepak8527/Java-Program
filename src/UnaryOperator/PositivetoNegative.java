package UnaryOperator;

public class PositivetoNegative {

	public static void main(String[]args) {
	// convert positive value to negative value	
		int x = 10;
		int y = -x;
		System.out.println(y);
		// negativ to positive value are convert
		int a = -10;
		int b = -a;
		System.out.println(b);
		// Preincrement are the increment value by 1
		int z = 10;
		int y1 = ++z;
		System.out.println("preincrement is: " + y1);
		// Preincrement are the Decrement value by 1
		
		int xy = 10;
		int yx = --xy;
		System.out.println("Predecrement are: " + yx);
		
	}
}
