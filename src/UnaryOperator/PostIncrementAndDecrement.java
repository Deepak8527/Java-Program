package UnaryOperator;

public class PostIncrementAndDecrement {

	public static void main(String[]args) {
	// postincrement is use the statement after the increment value 1
		
		int x = 10;
		int y = x++;
		System.out.println(y);
		
		// Postdecrement is use of statement are the decrement value by 1
		
		int xy = 10;
		int yx = xy--;
		System.out.println(yx);
	
	}    
}
